  var _stylesInjected = false;
  var _containerId = null;

  var RealDealAcademy = {

    init: function() {
      if (!_stylesInjected) {
        var tag = document.createElement('style');
        tag.id = 'rda-academy-styles';
        tag.textContent = getStyles();
        document.head.appendChild(tag);
        _stylesInjected = true;
      }
    },

    renderAcademy: function(containerId) {
      _containerId = containerId;
      var el = document.getElementById(containerId);
      if (!el) return;
      this._renderCatalog(el);
    },

    // ── Catalog View ──
    _renderCatalog: function(el) {
      var done = getCompletedCount();
      var total = CHAPTERS.length;
      var pct = Math.round((done / total) * 100);

      var html = '<div class="rda-container">';

      // Header
      html += '<div class="rda-header">';
      html += '<h1 class="rda-header-title"><span class="rda-accent">LOCAL AUTHORITY</span> BLUEPRINT</h1>';
      html += '<p class="rda-header-sub">The 2026 Masterclass Series by Joe Deal</p>';
      html += '<div class="rda-series-badge">12-Chapter Masterclass</div>';
      html += '</div>';

      // Progress
      html += '<div class="rda-overall-progress">';
      html += '<div class="rda-progress-label">' + done + ' of ' + total + ' chapters completed (' + pct + '%)</div>';
      html += '<div class="rda-progress-track"><div class="rda-progress-fill" style="width:' + pct + '%"></div></div>';
      html += '</div>';

      // Grid
      html += '<div class="rda-grid">';
      for (var i = 0; i < CHAPTERS.length; i++) {
        var ch = CHAPTERS[i];
        var isDone = isChapterComplete(ch.id);
        html += '<div class="rda-card' + (isDone ? ' completed' : '') + '" data-ch="' + ch.id + '">';

        html += '<div class="rda-card-top">';
        html += '<div class="rda-card-num">' + (ch.id < 10 ? '0' : '') + ch.id + '</div>';
        html += '<div class="rda-card-info">';
        html += '<div class="rda-card-title">' + ch.title + '</div>';
        html += '<div class="rda-card-subtitle">' + ch.subtitle + '</div>';
        html += '</div></div>';

        html += '<div class="rda-card-body">';
        html += '<ul class="rda-card-topics">';
        for (var t = 0; t < ch.topics.length; t++) {
          html += '<li>' + ch.topics[t] + '</li>';
        }
        html += '</ul>';
        html += '<div class="rda-card-status">';
        if (isDone) {
          html += '<span class="done">&#10003; Completed</span>';
        } else {
          html += '<span class="pending">Start Chapter &rarr;</span>';
        }
        html += '</div>';
        html += '</div>';

        html += '</div>';
      }
      html += '</div>';
      html += '</div>';

      el.innerHTML = html;

      // Attach click handlers
      var cards = el.querySelectorAll('.rda-card');
      var self = this;
      for (var c = 0; c < cards.length; c++) {
        (function(card) {
          card.addEventListener('click', function() {
            var chId = parseInt(card.getAttribute('data-ch'), 10);
            self._renderLesson(el, chId);
          });
        })(cards[c]);
      }
    },

    // ── Lesson / Reading View ──
    _renderLesson: function(el, chapterId) {
      var ch = null;
      for (var i = 0; i < CHAPTERS.length; i++) {
        if (CHAPTERS[i].id === chapterId) { ch = CHAPTERS[i]; break; }
      }
      if (!ch) return;

      var isDone = isChapterComplete(ch.id);

      var html = '<div class="rda-container"><div class="rda-lesson">';

      // Back button
      html += '<button class="rda-back-btn" id="rda-back">&larr; Back to Chapters</button>';

      // Lesson header
      html += '<div class="rda-lesson-head">';
      html += '<div class="rda-lesson-chnum">Chapter ' + ch.id + ' of 12</div>';
      html += '<h1 class="rda-lesson-title">' + ch.title + '</h1>';
      html += '<p class="rda-lesson-subtitle">' + ch.subtitle + '</p>';
      html += '</div>';

      // Content
      html += '<div class="rda-lesson-body">' + ch.content + '</div>';

      // Complete button
      if (isDone) {
        html += '<button class="rda-complete-btn done" id="rda-mark">&#10003; Chapter Completed</button>';
      } else {
        html += '<button class="rda-complete-btn mark" id="rda-mark">Mark Chapter as Complete</button>';
      }

      html += '</div></div>';

      el.innerHTML = html;
      el.scrollTop = 0;

      // scroll the parent view to top as well
      var scrollParent = el.closest('.view-content') || el.parentElement;
      if (scrollParent) scrollParent.scrollTop = 0;

      // Back handler
      var self = this;
      var backBtn = document.getElementById('rda-back');
      if (backBtn) {
        backBtn.addEventListener('click', function() {
          self._renderCatalog(el);
        });
      }

      // Complete handler
      var markBtn = document.getElementById('rda-mark');
      if (markBtn) {
        markBtn.addEventListener('click', function() {
          toggleChapterComplete(ch.id);
          self._renderLesson(el, ch.id);
        });
      }
    }
  };

