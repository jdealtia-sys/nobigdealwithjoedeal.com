  function loadProgress() {
    try {
      const raw = localStorage.getItem(STORAGE_KEY);
      if (raw) return JSON.parse(raw);
    } catch (_) { /* ignore */ }
    return { completed: [] };
  }

  function saveProgress(data) {
    try {
      localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
    } catch (_) { /* ignore */ }
  }

  function isChapterComplete(id) {
    return loadProgress().completed.indexOf(id) !== -1;
  }

  function toggleChapterComplete(id) {
    var progress = loadProgress();
    var idx = progress.completed.indexOf(id);
    if (idx === -1) {
      progress.completed.push(id);
    } else {
      progress.completed.splice(idx, 1);
    }
    saveProgress(progress);
    return progress;
  }

  function getCompletedCount() {
    return loadProgress().completed.length;
  }

